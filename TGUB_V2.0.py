@app.on_message(filters.command("mk", prefixes=".") & filters.me)
def mk(_, msg):
    orig_text = msg.text.split(".mk ", maxsplit=1)[1]
    text = orig_text
    tbp = "" # to be printed
    typing_symbol = "|"
 
    while(tbp != orig_text):
        try:
            msg.edit(tbp + typing_symbol)
            sleep(0.05) # 50 ms
 
            tbp = tbp + text[0]
            text = text[1:]
 
            msg.edit(tbp)
            sleep(0.05)
 
        except FloodWait as e:
            sleep(e.x)
 

